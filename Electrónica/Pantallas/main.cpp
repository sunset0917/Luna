// =========================
// CALLBACK DE DATOS
// =========================
void processData(AsyncResult &aResult) {
  if (aResult.isError()) {
    // Imprime el error si ocurre
    Serial.printf("Error: %s\n", aResult.error().message().c_str());
    return;
  }

  if (aResult.available()) {
      Serial.print("[RUTA] "); Serial.println(aResult.path().c_str());
      String data = aResult.payload();
      Serial.print("[VALOR] "); Serial.println(data);
      
      if(data != "null") {
        valorEmocion = data.toInt();
        nuevoDatoRecibido = true;
      }
  }
}

// =========================
// SETUP
// =========================
void setup() {
  Serial.begin(115200);

  // INICIAR OLED
  if(!oled.begin(SSD1306_SWITCHCAPVCC, 0x3C)) { 
    Serial.println(F("Error OLED")); for(;;);
  }
  oled.clearDisplay();
  oled.setTextSize(1);
  oled.setTextColor(SSD1306_WHITE);
  oled.setCursor(0,0);
  oled.println("Conectando WiFi...");
  oled.display();

  // INICIAR WIFI
  WiFi.begin(WIFI_SSID, WIFI_PASSWORD);
  while (WiFi.status() != WL_CONNECTED) {
    delay(300); Serial.print(".");
  }
  Serial.println("\nWiFi OK");

  // SSL INSEGURO (necesario si no sincronizamos fecha/hora estrictamente)
  ssl_client.setInsecure();

  Serial.println("Iniciando Firebase...");

  // 1. INICIALIZAR APP
  initializeApp(aClient, app, getAuth(no_auth));

  // 2. VINCULAR DATABASE (ESTO SOLUCIONA EL ERROR "APP NOT ASSIGNED")
  app.getApp<RealtimeDatabase>(Database);

  // 3. URL
  Database.url(DATABASE_URL);
  
  oled.clearDisplay();
  oled.setCursor(0,0);
  oled.println("Firebase Listo!");
  oled.display();
}

