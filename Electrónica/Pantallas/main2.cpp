void moverSuave(int fin) {
  fin = constrain(fin, 5, 175);
  int inicio = posicionActualServo;

  if (inicio < fin) {
    for (int pos = inicio; pos <= fin; pos++) {
      servo1.write(pos);
      delay(10);
    }
  } else {
    for (int pos = inicio; pos >= fin; pos--) {
      servo1.write(pos);
      delay(10);
    }
  }

  posicionActualServo = fin;
}

void negarCabeza() {
  int centro = posicionActualServo;
  int derecha = constrain(centro + amplitud, 5, 175);
  int izquierda = constrain(centro - amplitud, 5, 175);

  for (int pos = centro; pos <= derecha; pos++) {
    servo1.write(pos);
    delay(10);
  }
  for (int pos = derecha; pos >= izquierda; pos--) {
    servo1.write(pos);
    delay(10);
  }
  for (int pos = izquierda; pos <= centro; pos++) {
    servo1.write(pos);
    delay(10);
  }

  posicionActualServo = centro;
}

void mirarIzquierda() {
  int centro = posicionActualServo;
  moverSuave(centro - amplitud);
  delay(2000);
  moverSuave(centro);
}

void mirarDerecha() {
  int centro = posicionActualServo;
  moverSuave(centro + amplitud);
  delay(2000);
  moverSuave(centro);
}

